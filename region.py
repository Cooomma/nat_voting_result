class Regions():

    def __init__(self):
        global raw_regions
        self.raw_regions = raw_regions
        self.set_regions()

    def __repr__(self):
        return str(self.__dict__)

    def set_regions(self):
        self.regions = []
        for county, params in self.raw_regions.items():
            regions = self.set_region_by_county(county, params)
            self.regions.extend(regions)

    def set_region_by_county(self, county, params):
        n = int(len(params['districts']) / 2)
        regions = []
        for i in range(0, n):
            url = params['pre'] + '%02d' % i + params['post'] + '.html'
            name = county + params['districts'][i*2:(i+1)*2]
            regions.append(Region(name, url))
        return regions


class Region():

    def __init__(self, name, url):
        self.name = name
        self.url = url

    def __repr__(self):
        return str(self.__dict__)
